package day04;

public class NestingForEx04 {

	public static void main(String[] args) {
		/*
		 * *찍는 문제 5X5
		 */

		for (int j = 0; j < 5; j++) { // 세로별
			for (int i = 0; i < 7; i++) { // 가로별
				System.out.print("*");
			}
			System.out.println();
		}

		System.out.println("--------");
		
		
	}

}
